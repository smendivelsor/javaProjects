
package compoundoperators;

public class Compoundoperators {

    public static void main(String[] args) {
        int numCookies = 17;
        numCookies -= 3;
        System.out.println(numCookies);
        numCookies /= 2;
        
        System.out.println(numCookies);
    }
    
}
